# 1 Выяснить тип результата выражений:
# 15 * 3
# 15 / 3
# 15 // 2
# 15 ** 2

math_express1 = 15 * 3
math_express2 = 15 / 3
math_express3 = 15 // 2
math_express4 = 15 ** 2

print('results of mathematical expression: 15 * 3', type(math_express1))
print('results of mathematical expression: 15 / 3', type(math_express2))
print('results of mathematical expression: 15 // 2', type(math_express3))
print('results of mathematical expression: 15 ** 2', type(math_express4))
